// arbitrary input/output len
pub fn shake256(output: &mut [u8], input: &[u8]) {
    let mut shake_hash_fn = Shake256::default();
    shake_hash_fn.update(input);

    let mut result_shake = shake_hash_fn.finalize_xof();
    result_shake.read(output);
}

